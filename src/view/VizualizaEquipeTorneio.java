    private void btn_listaEquipesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_listaEquipesActionPerformed
        // TODO add your handling code here:

        DefaultTableModel dtmEquipes = (DefaultTableModel) tbl_equipes.getModel();
        String torneioSelecionado = cb_listaTorneios.getSelectedItem().toString();

        TorneioHasEquipeDAO daoTorneioHasEquipe = new TorneioHasEquipeDAO();

        ArrayList<String> equipes = daoTorneioHasEquipe.pesquisaEquipeTorneio(torneioSelecionado);
        ArrayList<String> capitaes = daoTorneioHasEquipe.pesquisaCapitaoEquipe(torneioSelecionado);

        dtmEquipes.setNumRows(0);

        for (int i = 0; i < equipes.size(); i++) {
            if (!capitaes.isEmpty()) {
                Object[] dados = {
                    equipes.get(i),
                    capitaes.get(i)
                };
                dtmEquipes.addRow(dados);
            } else {
                Object[] dados = {
                    equipes.get(i)
                };
                dtmEquipes.addRow(dados);
            }
        }

    }//GEN-LAST:event_btn_listaEquipesActionPerformed

